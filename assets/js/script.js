function startQuiz() {
var timeLeft = 120;
    var timeInterval = setInterval(function() {

        if(timeLeft >= 1) {
          timerEl.textContent = timeLeft
          timeLeft--;
        } else {
          timerEl.textContent = ""
          clearInterval(timeInterval);
        window.alert("Time Out!");
        }
    }, 1000);

playQuiz();
};

startBtn.addEventListener("click", startQuiz);